    private void JBsavalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBsavalActionPerformed
 
    // Criar uma instÃ¢ncia de Paciente
    Paciente paciente = new Paciente();
    //
    // Preencher os atributos do paciente com os valores dos campos
    paciente.setNome(JTnome.getText());
    paciente.setidade(Integer.parseInt(JTidade.getText()));
    paciente.setSexo(JTsexo.getText());
    paciente.setCpf(JTcpf.getText());
    paciente.setRua(JTrua.getText());
    paciente.setNumero(Integer.parseInt(JTnumero.getText()));
    paciente.setComplemento(JTcom.getText());
    paciente.setBairro(JTbairro.getText());
    paciente.setEstado(JTestado.getText());
    paciente.setContato(JTcontato.getText());
    paciente.setEmail(JTemail.getText());
    paciente.setDataNascimento(Date.valueOf(JTdadaN.getText()));
    
   
    try {
        PacienteDao pacienteDao = new PacienteDao();
        pacienteDao.save(paciente);
        JOptionPane.showMessageDialog(this, "Paciente salvo com sucesso!");
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar o paciente: " + ex.getMessage());
    }
}

